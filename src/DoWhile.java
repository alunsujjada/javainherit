public class DoWhile {
    
    public static void main(String[] args) {
         int bil = 11;
         
         do{
             System.out.println(bil);
             bil++;
         }while (bil<=10);
         
         System.out.println("----------------------------------");
         int angka = 11;
         
         while(angka <=10){
             System.out.println(angka);
             angka++;
         }
    }
    
}
